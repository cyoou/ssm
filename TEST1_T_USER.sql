CREATE TABLE TEST1.T_USER
(
    ID NUMBER(*) PRIMARY KEY NOT NULL,
    USERNAME VARCHAR2(4000) NOT NULL,
    PASSWORD VARCHAR2(4000) NOT NULL,
    BIRTHDAY DATE NOT NULL,
    REGTIME TIMESTAMP(6) NOT NULL
);
COMMENT ON COLUMN TEST1.T_USER.ID IS '主键ID';
COMMENT ON COLUMN TEST1.T_USER.USERNAME IS '用户名';
COMMENT ON COLUMN TEST1.T_USER.PASSWORD IS '密码';
COMMENT ON COLUMN TEST1.T_USER.BIRTHDAY IS '生日';
COMMENT ON COLUMN TEST1.T_USER.REGTIME IS '注册时间';
CREATE UNIQUE INDEX USER_USERNAME_UINDEX ON TEST1.T_USER (USERNAME);
